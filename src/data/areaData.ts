import { REAL_BOUNDARIES } from "./realBoundaries";
export interface AreaData {
  id: string;
  name: string;
  city: string;
  lat: number;
  lng: number;
  polygon_coords: [number, number][];
  current_price_psft: number; //Current average property price per square foot
  quarterly_prices: {
    quarter: string;
    price_psft: number;
  }[];
  rental_yield_pct: number; //Annual rental income as a percentage of property value
  yoy_change_pct: number; //Year-over-Year price appreciation
  connectivity_score: number; //Score for road, metro, airport and public transport access
  infrastructure_score: number; //Quality of existing and upcoming infrastructure
  property_types_available: ("apartment" | "villa" | "plot")[]; //Types of properties available in the area
  days_on_market_avg: number; //Average number of days a property takes to sell
  unsold_inventory_months: number; //How many months it would take to sell all available inventory at the current sales rate
  new_launches_last_quarter: number; //Number of new residential projects launched recently
  enquiries_per_listing: number; //Average buyer enquiries received per property listing
  five_year_cagr_pct: number; //Average annual growth rate over the last 5 years
  liveability_score: number; //Quality of life score (schools, hospitals, parks, retail, safety)
  nri_interest_score: number; //Measures how attractive the area is to NRI investors
  key_employers_nearby: string[]; //Major companies/business hubs driving housing demand
  upcoming_infrastructure: string[]; //Future developments expected to impact property values
  zone: "north" | "south" | "east" | "west" | "central";
}

export type MarketTemperature = "hot" | "warm" | "balanced" | "cooling";

export function getMarketTemperature(area: AreaData): MarketTemperature {
  if (area.days_on_market_avg < 35 && area.unsold_inventory_months < 4)
    return "hot";
  if (area.days_on_market_avg < 55 && area.unsold_inventory_months < 6)
    return "warm";
  if (area.days_on_market_avg > 75 || area.unsold_inventory_months > 9)
    return "cooling";
  return "balanced";
}

export function getPolygon(
  id: string,
  fallback: [number, number][],
): [number, number][] {
  return REAL_BOUNDARIES[id] ?? fallback;
}

